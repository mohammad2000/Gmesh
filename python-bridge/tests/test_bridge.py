"""End-to-end bridge tests against a live gmeshd subprocess."""

from __future__ import annotations

import asyncio
import json

import pytest

from gmesh_bridge import EventForwarder, GmeshBridge, Translator
from gmesh_bridge.translator import UnknownMessageType, known_message_types


@pytest.mark.asyncio
async def test_version_and_status(daemon):
    br = await GmeshBridge.connect(socket_path=daemon["socket"])
    try:
        v = await br.version()
        assert v["version"]
        st = await br.status()
        assert st["joined"] is False
        assert st["peer_count"] == 0
    finally:
        await br.close()


@pytest.mark.asyncio
async def test_translator_scope_roundtrip(daemon):
    br = await GmeshBridge.connect(socket_path=daemon["socket"])
    t = Translator(br)
    try:
        resp = await t.handle({
            "type": "scope_mesh_connect",
            "scope_id": 77,
            "scope_mesh_ip": "10.200.0.77",
            "veth_cidr": "10.50.77.0/30",
            "vm_veth_ip": "10.50.77.1",
            "scope_ip": "10.50.77.2",
            "listen_port": 51877,
        })
        assert resp["type"] == "scope_mesh_connected"
        assert resp["success"] is True
        assert resp["scope_id"] == 77
        assert resp["public_key"]  # generated by gmeshd

        off = await t.handle({"type": "scope_mesh_disconnect", "scope_id": 77})
        assert off["type"] == "scope_mesh_disconnected"
        assert off["success"] is True
    finally:
        await br.close()


@pytest.mark.asyncio
async def test_translator_firewall_roundtrip(daemon):
    br = await GmeshBridge.connect(socket_path=daemon["socket"])
    t = Translator(br)
    try:
        resp = await t.handle({
            "type": "mesh_firewall_apply",
            "default_policy": "accept",
            "rules": [
                {"id": 1, "name": "allow ssh", "enabled": True, "priority": 100,
                 "action": 1, "protocol": 2, "port_range": "22",
                 "direction": "inbound"},
            ],
        })
        assert resp["type"] == "mesh_firewall_applied"
        assert resp["success"] is True
        assert resp["applied_count"] == 1

        status = await t.handle({"type": "mesh_firewall_status"})
        assert status["type"] == "mesh_firewall_status"
        assert status["active_rules"] == 1

        reset = await t.handle({"type": "mesh_firewall_reset"})
        assert reset["success"] is True
    finally:
        await br.close()


